clear;
x = 0:pi/100:2*pi;                       %自变量取值范围[0,2π]，间隔π/100
y1 = sin(x);                             %正弦函数
y2 = sin(x-0.25);                        %原始正弦函数x向右移动0.25对应的函数
y3 = sin(x-0.5);                         %原始正弦函数x向右移动0.5对应的函数

figure                                   %打开图窗口
plot(x,y1,x,y2,'--',x,y3,':')            %绘制不同线型
